import requests
                
def ip():
    api_url = f"https://get.geojs.io/v1/ip/geo.js"
    querystring = {}
    response = requests.get(url=api_url, params=querystring, timeout=50)
    if response.status_code != 200:
        print("--------------------------------------------")
        print(response.status_code)
        print(response.text)
    # print(response.json())
ip()

