import requests
                
def get_addresses():
    api_url = f"https://fakerapi.it/api/v1/addresses"
    querystring = {}
    response = requests.get(url=api_url, params=querystring, timeout=50)
    if response.status_code != 200:
        print("--------------------------------------------")
        print(response.status_code)
        print(response.text)
    # print(response.json())
get_addresses()

