import requests
                
def get_dólar_oficial():
    api_url = f"https://dolarapi.com/dolar/oficial"
    querystring = {}
    response = requests.get(url=api_url, params=querystring, timeout=50)
    if response.status_code != 200:
        print("--------------------------------------------")
        print(response.status_code)
        print(response.text)
    # print(response.json())
get_dólar_oficial()

