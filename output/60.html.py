import requests
                
def get_metadata_of_all_available_datasets():
    api_url = f"https://data.overheid.nl/data/api/3/"
    querystring = {}
    response = requests.get(url=api_url, params=querystring, timeout=50)
    if response.status_code != 200:
        print("--------------------------------------------")
        print(response.status_code)
        print(response.text)
    # print(response.json())
get_metadata_of_all_available_datasets()

